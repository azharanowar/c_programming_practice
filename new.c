#include <stdio.h>

int main(void) {
    // Declare and initiate integer variable
    int integerVar = 100;
    printf("integerVar (integer): %i \n", integerVar);

    // Declare and initiate float variable
    float floatingVar = 200.10;
    printf("floatingVar (float): %f\n", floatingVar);

    // Declare and initiate double variable
    double doubleVar = 52200.2010510; 
    printf("doubleVar (double): %g\n", doubleVar);

    // Declare and initiate char variable
    char charVar = 'A';
    printf("charVar (char): %c\n", charVar);

    // Declare and initiate boolean variable
    _Bool boolVar = 1; // Represent true value...
    printf("boolVar (0 = false, 1 = true): %i\n", boolVar);


    return 0;
}